CREATE TABLE IF NOT EXISTS `question_duplicate_eval_case` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Primary key',
  `case_id` VARCHAR(128) NOT NULL COMMENT 'Human readable eval case id',
  `source_question_id` BIGINT NOT NULL COMMENT 'Source question id',
  `target_question_id` BIGINT NOT NULL COMMENT 'Target question id',
  `expected` VARCHAR(32) NOT NULL COMMENT 'DUPLICATE, REVIEW, NOT_DUPLICATE',
  `note` VARCHAR(512) DEFAULT NULL COMMENT 'Case note',
  `enabled` TINYINT NOT NULL DEFAULT 1 COMMENT '1 enabled, 0 disabled',
  `created_by` BIGINT DEFAULT NULL COMMENT 'Creator user id',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '0 active, 1 deleted',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_question_dup_eval_case_id` (`case_id`, `deleted`),
  KEY `idx_question_dup_eval_pair` (`source_question_id`, `target_question_id`),
  KEY `idx_question_dup_eval_expected` (`expected`, `enabled`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Question duplicate evaluation cases';

CREATE TABLE IF NOT EXISTS `question_duplicate_eval_run` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Primary key',
  `run_no` VARCHAR(64) NOT NULL COMMENT 'Eval run number',
  `status` VARCHAR(32) NOT NULL COMMENT 'RUNNING, SUCCESS, FAILED',
  `sample_count` INT NOT NULL DEFAULT 0 COMMENT 'Sample count',
  `evaluated_count` INT NOT NULL DEFAULT 0 COMMENT 'Evaluated count',
  `passed_count` INT NOT NULL DEFAULT 0 COMMENT 'Passed count',
  `failed_count` INT NOT NULL DEFAULT 0 COMMENT 'Failed count',
  `missing_question_count` INT NOT NULL DEFAULT 0 COMMENT 'Missing question count',
  `accuracy_rate` DECIMAL(6,2) DEFAULT NULL COMMENT 'Accuracy percent',
  `started_at` DATETIME DEFAULT NULL COMMENT 'Run start time',
  `finished_at` DATETIME DEFAULT NULL COMMENT 'Run finish time',
  `created_by` BIGINT DEFAULT NULL COMMENT 'Operator user id',
  `error_message` VARCHAR(512) DEFAULT NULL COMMENT 'Error message',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '0 active, 1 deleted',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_question_dup_eval_run_no` (`run_no`),
  KEY `idx_question_dup_eval_run_status` (`status`, `created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Question duplicate evaluation runs';

CREATE TABLE IF NOT EXISTS `question_duplicate_eval_result` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Primary key',
  `run_id` BIGINT NOT NULL COMMENT 'Eval run id',
  `eval_case_id` BIGINT DEFAULT NULL COMMENT 'Eval case id',
  `case_id` VARCHAR(128) DEFAULT NULL COMMENT 'Human readable eval case id',
  `source_question_id` BIGINT DEFAULT NULL COMMENT 'Source question id',
  `target_question_id` BIGINT DEFAULT NULL COMMENT 'Target question id',
  `expected` VARCHAR(32) DEFAULT NULL COMMENT 'Expected label',
  `predicted` VARCHAR(32) DEFAULT NULL COMMENT 'Predicted label',
  `passed` TINYINT NOT NULL DEFAULT 0 COMMENT '1 passed, 0 failed',
  `match_type` VARCHAR(64) DEFAULT NULL COMMENT 'Matched duplicate type',
  `score` DECIMAL(6,2) DEFAULT NULL COMMENT 'Final score percent',
  `score_band` VARCHAR(32) DEFAULT NULL COMMENT 'STRONG, REVIEW',
  `score_parts_json` LONGTEXT DEFAULT NULL COMMENT 'Structured score parts JSON',
  `reason` VARCHAR(1000) DEFAULT NULL COMMENT 'Failure or match reason',
  `note` VARCHAR(512) DEFAULT NULL COMMENT 'Case note',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '0 active, 1 deleted',
  PRIMARY KEY (`id`),
  KEY `idx_question_dup_eval_result_run` (`run_id`, `passed`),
  KEY `idx_question_dup_eval_result_case` (`eval_case_id`),
  KEY `idx_question_dup_eval_result_score` (`score_band`, `score`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Question duplicate evaluation results';
