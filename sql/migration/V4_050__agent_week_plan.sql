CREATE TABLE IF NOT EXISTS `agent_week_plan` (
    `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'primary key',
    `user_id` BIGINT NOT NULL COMMENT 'owner user id',
    `target_job_id` BIGINT DEFAULT NULL COMMENT 'target job id, nullable for all-scope plan',
    `agent_run_id` BIGINT DEFAULT NULL COMMENT 'primary agent_run id when derived from an existing run',
    `target_scope_key` VARCHAR(64) NOT NULL DEFAULT 'ALL' COMMENT 'stable unique scope key for nullable target',
    `week_start_date` DATE NOT NULL COMMENT 'week start date',
    `week_end_date` DATE NOT NULL COMMENT 'week end date',
    `plan_status` VARCHAR(32) NOT NULL DEFAULT 'ACTIVE' COMMENT 'DRAFT/ACTIVE/REFRESHED/COMPLETED/ARCHIVED',
    `summary` VARCHAR(1000) DEFAULT NULL COMMENT 'safe summary',
    `focus_json` TEXT DEFAULT NULL COMMENT 'safe focus snapshot, no raw resume/JD/prompt',
    `trace_id` VARCHAR(128) DEFAULT NULL COMMENT 'primary trace id',
    `result_source` VARCHAR(32) NOT NULL DEFAULT 'RULE' COMMENT 'RULE/LLM/FALLBACK/MOCK',
    `fallback` TINYINT NOT NULL DEFAULT 0 COMMENT 'whether generated by fallback rules',
    `fallback_reason` VARCHAR(500) DEFAULT NULL COMMENT 'fallback reason',
    `snapshot_version` INT NOT NULL DEFAULT 1 COMMENT 'snapshot version',
    `generated_at` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'generated time',
    `refreshed_at` DATETIME DEFAULT NULL COMMENT 'last refresh time',
    `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'created time',
    `updated_at` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'updated time',
    `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT 'logic delete flag',
    PRIMARY KEY (`id`),
    UNIQUE KEY `uk_awp_live_week_scope` (`user_id`, `week_start_date`, `target_scope_key`, `deleted`),
    KEY `idx_awp_user_week` (`user_id`, `week_start_date`, `week_end_date`, `deleted`, `updated_at`),
    KEY `idx_awp_trace` (`trace_id`, `deleted`),
    KEY `idx_awp_run` (`agent_run_id`, `deleted`),
    KEY `idx_awp_target` (`user_id`, `target_job_id`, `deleted`, `updated_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='agent persisted week plan';

CREATE TABLE IF NOT EXISTS `agent_week_plan_item` (
    `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'primary key',
    `week_plan_id` BIGINT NOT NULL COMMENT 'agent_week_plan id',
    `user_id` BIGINT NOT NULL COMMENT 'owner user id',
    `layer` VARCHAR(32) NOT NULL COMMENT 'TODAY/WEEK/NEXT_EXPERIMENT',
    `action_type` VARCHAR(64) NOT NULL DEFAULT 'AGENT_TASK' COMMENT 'action type',
    `title` VARCHAR(255) NOT NULL COMMENT 'safe title',
    `description` VARCHAR(1000) DEFAULT NULL COMMENT 'safe description',
    `reason` VARCHAR(1000) DEFAULT NULL COMMENT 'safe reason',
    `related_biz_type` VARCHAR(64) DEFAULT NULL COMMENT 'related business type',
    `related_biz_id` BIGINT DEFAULT NULL COMMENT 'related business id',
    `related_biz_title` VARCHAR(255) DEFAULT NULL COMMENT 'safe related title',
    `agent_task_id` BIGINT DEFAULT NULL COMMENT 'linked agent task id',
    `priority` VARCHAR(16) DEFAULT NULL COMMENT 'HIGH/MEDIUM/LOW',
    `confidence` DECIMAL(5,2) NOT NULL DEFAULT 0.50 COMMENT '0.00-1.00 confidence',
    `trust_status` VARCHAR(32) NOT NULL DEFAULT 'PARTIAL' COMMENT 'VERIFIED/PARTIAL/FALLBACK',
    `fallback` TINYINT NOT NULL DEFAULT 0 COMMENT 'fallback item',
    `fallback_reason` VARCHAR(500) DEFAULT NULL COMMENT 'fallback reason',
    `trace_id` VARCHAR(128) DEFAULT NULL COMMENT 'item trace id, normally linked agent_run trace',
    `snapshot_version` INT NOT NULL DEFAULT 1 COMMENT 'week plan snapshot version when item was created',
    `confidence_level` VARCHAR(16) NOT NULL DEFAULT 'MEDIUM' COMMENT 'HIGH/MEDIUM/LOW/UNKNOWN',
    `sample_insufficient` TINYINT NOT NULL DEFAULT 0 COMMENT 'whether item is based on low sample evidence',
    `sample_warning` VARCHAR(500) DEFAULT NULL COMMENT 'safe low sample warning',
    `item_status` VARCHAR(32) NOT NULL DEFAULT 'TODO' COMMENT 'TODO/IN_PROGRESS/DONE/SKIPPED/DEFERRED/CANCELLED',
    `planned_date` DATE DEFAULT NULL COMMENT 'planned date',
    `due_date` DATE DEFAULT NULL COMMENT 'linked task due date',
    `action_url` VARCHAR(500) DEFAULT NULL COMMENT 'safe in-app action url',
    `evidence_json` TEXT DEFAULT NULL COMMENT 'safe evidence summary list',
    `sort_order` INT NOT NULL DEFAULT 0 COMMENT 'sort order',
    `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'created time',
    `updated_at` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'updated time',
    `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT 'logic delete flag',
    PRIMARY KEY (`id`),
    KEY `idx_awpi_plan_layer` (`week_plan_id`, `layer`, `deleted`, `sort_order`),
    KEY `idx_awpi_trace` (`trace_id`, `deleted`),
    KEY `idx_awpi_user_task` (`user_id`, `agent_task_id`, `deleted`),
    KEY `idx_awpi_biz` (`user_id`, `related_biz_type`, `related_biz_id`, `deleted`),
    KEY `idx_awpi_status_date` (`user_id`, `item_status`, `planned_date`, `deleted`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='agent persisted week plan item';

