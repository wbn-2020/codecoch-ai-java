CREATE TABLE IF NOT EXISTS `personal_knowledge_eval_case` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Primary key',
  `user_id` BIGINT NOT NULL COMMENT 'Owner user id',
  `case_id` VARCHAR(128) NOT NULL COMMENT 'Human readable eval case id',
  `query_text` VARCHAR(1000) NOT NULL COMMENT 'Retrieval query',
  `expected_document_id` BIGINT DEFAULT NULL COMMENT 'Expected document id',
  `expected_document_title` VARCHAR(255) DEFAULT NULL COMMENT 'Expected document title snapshot',
  `expected_document_type` VARCHAR(64) DEFAULT NULL COMMENT 'Expected document type',
  `expect_no_answer` TINYINT NOT NULL DEFAULT 0 COMMENT '1 expects no answer, 0 expects a source hit',
  `note` VARCHAR(512) DEFAULT NULL COMMENT 'Case note',
  `enabled` TINYINT NOT NULL DEFAULT 1 COMMENT '1 enabled, 0 disabled',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '0 active, 1 deleted',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_personal_knowledge_eval_case_id` (`user_id`, `case_id`, `deleted`),
  KEY `idx_personal_knowledge_eval_case_enabled` (`user_id`, `enabled`, `updated_at`),
  KEY `idx_personal_knowledge_eval_case_document` (`user_id`, `expected_document_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Personal knowledge RAG evaluation cases';

CREATE TABLE IF NOT EXISTS `personal_knowledge_eval_run` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Primary key',
  `user_id` BIGINT NOT NULL COMMENT 'Owner user id',
  `run_no` VARCHAR(64) NOT NULL COMMENT 'Eval run number',
  `status` VARCHAR(32) NOT NULL COMMENT 'RUNNING, SUCCESS, FAILED',
  `sample_count` INT NOT NULL DEFAULT 0 COMMENT 'Sample count',
  `evaluated_count` INT NOT NULL DEFAULT 0 COMMENT 'Evaluated count',
  `passed_count` INT NOT NULL DEFAULT 0 COMMENT 'Passed count',
  `failed_count` INT NOT NULL DEFAULT 0 COMMENT 'Failed count',
  `pass_rate` DOUBLE DEFAULT NULL COMMENT 'Pass rate percent',
  `limit_count` INT DEFAULT NULL COMMENT 'Retrieval topK used by the run',
  `min_score` DOUBLE DEFAULT NULL COMMENT 'Retrieval minimum score used by the run',
  `started_at` DATETIME DEFAULT NULL COMMENT 'Run start time',
  `finished_at` DATETIME DEFAULT NULL COMMENT 'Run finish time',
  `error_message` VARCHAR(512) DEFAULT NULL COMMENT 'Error message',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '0 active, 1 deleted',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_personal_knowledge_eval_run_no` (`run_no`),
  KEY `idx_personal_knowledge_eval_run_status` (`user_id`, `status`, `created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Personal knowledge RAG evaluation runs';

CREATE TABLE IF NOT EXISTS `personal_knowledge_eval_result` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Primary key',
  `user_id` BIGINT NOT NULL COMMENT 'Owner user id',
  `run_id` BIGINT NOT NULL COMMENT 'Eval run id',
  `eval_case_id` BIGINT DEFAULT NULL COMMENT 'Eval case id',
  `case_id` VARCHAR(128) DEFAULT NULL COMMENT 'Human readable eval case id',
  `query_text` VARCHAR(1000) DEFAULT NULL COMMENT 'Retrieval query',
  `expected_document_id` BIGINT DEFAULT NULL COMMENT 'Expected document id',
  `expected_document_title` VARCHAR(255) DEFAULT NULL COMMENT 'Expected document title snapshot',
  `expected_document_type` VARCHAR(64) DEFAULT NULL COMMENT 'Expected document type',
  `expect_no_answer` TINYINT NOT NULL DEFAULT 0 COMMENT '1 expects no answer, 0 expects a source hit',
  `passed` TINYINT NOT NULL DEFAULT 0 COMMENT '1 passed, 0 failed',
  `top_document_id` BIGINT DEFAULT NULL COMMENT 'Top retrieved document id',
  `top_title` VARCHAR(255) DEFAULT NULL COMMENT 'Top retrieved document title',
  `top_document_type` VARCHAR(64) DEFAULT NULL COMMENT 'Top retrieved document type',
  `top_score` DOUBLE DEFAULT NULL COMMENT 'Top reference score',
  `reference_count` INT NOT NULL DEFAULT 0 COMMENT 'Retrieved reference count',
  `failure_reason` VARCHAR(1000) DEFAULT NULL COMMENT 'Failure reason',
  `note` VARCHAR(512) DEFAULT NULL COMMENT 'Case note',
  `references_json` LONGTEXT DEFAULT NULL COMMENT 'Structured references JSON',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '0 active, 1 deleted',
  PRIMARY KEY (`id`),
  KEY `idx_personal_knowledge_eval_result_run` (`user_id`, `run_id`, `passed`),
  KEY `idx_personal_knowledge_eval_result_case` (`eval_case_id`),
  KEY `idx_personal_knowledge_eval_result_score` (`top_score`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Personal knowledge RAG evaluation results';
